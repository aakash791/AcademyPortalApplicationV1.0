package com.ad.acp.model;

public class BatchModel {

	String batid;
	String domid;
	String facid;
	String batchStartDate;
	String batchEndDate;
	String batchClosedDate;
	public String getBatid() {
		return batid;
	}
	public void setBatid(String batid) {
		this.batid = batid;
	}
	public String getDomid() {
		return domid;
	}
	public void setDomid(String domid) {
		this.domid = domid;
	}
	public String getFacid() {
		return facid;
	}
	public void setFacid(String facid) {
		this.facid = facid;
	}
	public String getBatchStartDate() {
		return batchStartDate;
	}
	public void setBatchStartDate(String batchStartDate) {
		this.batchStartDate = batchStartDate;
	}
	public String getBatchEndDate() {
		return batchEndDate;
	}
	public void setBatchEndDate(String batchEndDate) {
		this.batchEndDate = batchEndDate;
	}
	public String getBatchClosedDate() {
		return batchClosedDate;
	}
	public void setBatchClosedDate(String batchClosedDate) {
		this.batchClosedDate = batchClosedDate;
	}
	
	
}
